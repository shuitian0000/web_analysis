def create_exam_doc(year_data, paper_type, output_path):
    """生成试卷Word文档"""
    doc = Document()

    # 设置默认字体
    style = doc.styles['Normal']
    font = style.font
    font.name = '宋体'
    font.size = Pt(11)

    # 标题
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.add_run(f"{year_data['year']}年陕西省初中学业水平考试")
    run.font.size = Pt(16)
    run.bold = True
    run.font.name = '黑体'

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = subtitle.add_run(f"生物学试卷（{paper_type}卷）")
    run.font.size = Pt(14)
    run.bold = True
    run.font.name = '黑体'

    # 考试说明
    info = doc.add_paragraph()
    info.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = info.add_run("（本试卷满分60分，考试时间60分钟）")
    run.font.size = Pt(10)

    # 注意事项
    note = doc.add_paragraph()
    note.alignment = WD_ALIGN_PARAGRAPH.LEFT
    run = note.add_run("注意事项：1. 答卷前，请将姓名、准考证号填写在答题卡上。2. 选择题用2B铅笔填涂，非选择题用黑色签字笔作答。3. 考试结束后，将试卷和答题卡一并交回。")
    run.font.size = Pt(9)

    doc.add_paragraph()  # 空行

    # 选择题部分
    h1 = doc.add_paragraph()
    run = h1.add_run("一、选择题（本大题共25小题，每小题1分，共25分。每小题只有一个选项符合题意）")
    run.bold = True
    run.font.size = Pt(11)

    paper = year_data[paper_type]
    for q_text, options, _ in paper["choices"]:
        p = doc.add_paragraph()
        run = p.add_run(q_text)
        run.font.size = Pt(11)
        for opt in options:
            p2 = doc.add_paragraph()
            p2.paragraph_format.left_indent = Cm(1)
            run2 = p2.add_run(opt)
            run2.font.size = Pt(11)

    doc.add_page_break()

    # 非选择题部分
    h2 = doc.add_paragraph()
    run = h2.add_run("二、非选择题（本大题共6小题，共35分）")
    run.bold = True
    run.font.size = Pt(11)

    for q_text, _ in paper["non_choices"]:
        p = doc.add_paragraph()
        run = p.add_run(q_text)
        run.font.size = Pt(11)

    doc.save(output_path)


def create_answer_doc(year_data, paper_type, output_path):
    """生成答案解析Word文档"""
    doc = Document()

    style = doc.styles['Normal']
    font = style.font
    font.name = '宋体'
    font.size = Pt(11)

    # 标题
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = title.add_run(f"{year_data['year']}年陕西省初中学业水平考试")
    run.font.size = Pt(16)
    run.bold = True
    run.font.name = '黑体'

    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = subtitle.add_run(f"生物学试卷（{paper_type}卷）参考答案及解析")
    run.font.size = Pt(14)
    run.bold = True
    run.font.name = '黑体'

    doc.add_paragraph()

    # 选择题答案
    h1 = doc.add_paragraph()
    run = h1.add_run("一、选择题答案")
    run.bold = True
    run.font.size = Pt(12)

    paper = year_data[paper_type]

    # 答案速查表
    table = doc.add_table(rows=6, cols=5)
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    for i in range(25):
        row = i // 5
        col = (i % 5)
        cell = table.cell(row, col)
        q_num = i + 1
        answer = paper["choices"][i][2]
        cell.text = f"{q_num}.{answer}"

    doc.add_paragraph()

    # 选择题解析
    h1b = doc.add_paragraph()
    run = h1b.add_run("选择题解析")
    run.bold = True
    run.font.size = Pt(12)

    for q_text, options, answer in paper["choices"]:
        p = doc.add_paragraph()
        # 只取题号和题干（不含选项）
        q_line = q_text.split('\n')[0] if '\n' in q_text else q_text
        run = p.add_run(f"{q_line}")
        run.font.size = Pt(11)
        p2 = doc.add_paragraph()
        p2.paragraph_format.left_indent = Cm(0.5)
        run2 = p2.add_run(f"【答案】{answer}")
        run2.bold = True
        run2.font.size = Pt(11)
        run2.font.color.rgb = RGBColor(255, 0, 0)

    doc.add_page_break()

    # 非选择题答案
    h2 = doc.add_paragraph()
    run = h2.add_run("二、非选择题答案及解析")
    run.bold = True
    run.font.size = Pt(12)

    for q_text, answer_text in paper["non_choices"]:
        # 取题号
        q_line = q_text.split('\n')[0] if '\n' in q_text else q_text[:30]
        p = doc.add_paragraph()
        run = p.add_run(f"{q_line}…")
        run.font.size = Pt(11)
        p2 = doc.add_paragraph()
        p2.paragraph_format.left_indent = Cm(0.5)
        run2 = p2.add_run(f"【答案】{answer_text}")
        run2.bold = True
        run2.font.size = Pt(11)
        run2.font.color.rgb = RGBColor(255, 0, 0)

    doc.save(output_path)


def main():
    for year_key, year_data in EXAMS.items():
        year_dir = os.path.join(BASE_DIR, f"{year_key}年")
        os.makedirs(year_dir, exist_ok=True)

        for paper_type in ["A", "B"]:
            # 生成试卷
            exam_path = os.path.join(year_dir, f"{year_key}年陕西中考生物{paper_type}卷.docx")
            create_exam_doc(year_data, paper_type, exam_path)
            print(f"已生成: {exam_path}")

            # 生成答案解析
            answer_path = os.path.join(year_dir, f"{year_key}年陕西中考生物{paper_type}卷答案解析.docx")
            create_answer_doc(year_data, paper_type, answer_path)
            print(f"已生成: {answer_path}")

    print("\n全部文档生成完毕！")


if __name__ == "__main__":
    main()
